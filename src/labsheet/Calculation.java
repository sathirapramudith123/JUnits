package labsheet;

public class Calculation {
	public static int addition(int a, int b) {
		int total = a+b;
		return total;
	}
	
	public static int subtraction(int x, int y) {
		int result = x-y;
		return result;
	}
	
	public static int multiplication(int p, int q) {
		int result = p*q;
		return result;
	}
	
	public static int divition(int s, int k) {
		int result = s/k;
		return result;
	}
}

